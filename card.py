class card:

        def __init__(self, prestige, cost, color, tire):
                """
                """
                self.prestige = prestige
                self.cost = cost
                self.color = color
                self.tire = tire

        def getPrestige(self):
                return self.prestige

        def getCost(self):
                return self.cost

        def getColor(self):
                return self.color
        
        def getTire(self):
                return self.tire

